"""
Variable reassignment:We can Assign infinite values to the same variable that is called variable reassignment.
"""

x = 10
print(x)
#O/P: 10

x = 100
print(x)
#O/P: 100

x = "sangamesh" 
print(x)
#O/P: 'sangamesh'

x = 5.8
print(x)
#O/P: 5.8

x = True
print(x)
#O/P: True